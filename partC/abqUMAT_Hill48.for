    SUBROUTINE HYFUNCTION(SIGMA,NTENS,YF,GYF,HYF,KMATERIAL,NKMAT)
    IMPLICIT NONE
	INTEGER,PARAMETER::PREC=8

	INTEGER::NTENS,NKMAT
	REAL(PREC),DIMENSION(NTENS)::SIGMA,GYF
	REAL(PREC),DIMENSION(NTENS,NTENS)::HYF
	REAL(PREC),DIMENSION(NKMAT)::KMATERIAL
	REAL(PREC)::YF

    REAL(PREC)::GH,FH,H,N
	H=KMATERIAL(3)
    FH=KMATERIAL(1)+H	
!C  GH=KMATERIAL(2)+H
!C	N=2.0D0*KMATERIAL(4)
	N=KMATERIAL(4)
	YF=DSQRT(SIGMA(1)*SIGMA(1)-2.0D0*H*SIGMA(1)*SIGMA(2)+FH*SIGMA(2)*SIGMA(2)+N*SIGMA(3)*SIGMA(3))
	GYF(1)=(SIGMA(1)-H*SIGMA(2))/YF
	GYF(2)=(FH*SIGMA(2)-H*SIGMA(1))/YF
	GYF(3)=(N*SIGMA(3))/YF
    HYF(1,1)=dble(1.0e-12)+(1.0D0-GYF(1)*GYF(1))/YF
	HYF(2,1)=-(H+GYF(1)*GYF(2))/YF
	HYF(3,1)=-(GYF(3)*GYF(1))/YF
