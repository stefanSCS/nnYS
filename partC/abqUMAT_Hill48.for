	HYF(3,2)=-(GYF(2)*GYF(3))/YF
	HYF(1,3)=HYF(3,1)
	HYF(2,3)=HYF(3,2)
	HYF(3,3)=dble(1.0e-12)+(N-GYF(3)*GYF(3))/YF
	
	RETURN
	END SUBROUTINE HYFUNCTION

